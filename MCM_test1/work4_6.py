import pandas as pd
from scipy import stats

# 将数据输入为pandas DataFrame
data = {
    "实验室1": [4.13, 4.07, 4.04, 4.07, 4.05, 4.04, 4.02, 4.06, 4.10, 4.04],
    "实验室2": [3.86, 3.85, 4.08, 4.11, 4.08, 4.01, 4.02, 4.04, 3.97, 3.95],
    "实验室3": [4.00, 4.02, 4.01, 4.01, 4.04, 3.99, 4.03, 3.97, 3.98, 3.98],
    "实验室4": [3.88, 3.88, 3.91, 3.95, 3.92, 3.90, 3.92, 3.99, 3.90, 3.93],
    "实验室5": [4.02, 3.95, 4.02, 3.89, 3.91, 4.01, 3.89, 3.89, 3.99, 4.00],
    "实验室6": [4.02, 3.86, 3.96, 3.97, 4.00, 3.82, 3.98, 3.99, 4.02, 3.93],
    "实验室7": [4.00, 4.02, 4.03, 4.04, 4.10, 3.81, 3.91, 3.96, 4.05, 4.06]
}

df = pd.DataFrame(data)

# 使用 scipy 进行单因素方差分析
f_value, p_value = stats.f_oneway(df['实验室1'], df['实验室2'], df['实验室3'], df['实验室4'], df['实验室5'], df['实验室6'], df['实验室7'])

# 输出结果
print(f"F值: {f_value}")
print(f"P值: {p_value}")

# 显著性水平 alpha = 0.05
alpha = 0.05
if p_value < alpha:
    print("拒绝原假设，不同实验室的测量结果有显著性差异。")
else:
    print("不拒绝原假设，不同实验室的测量结果没有显著性差异。")
